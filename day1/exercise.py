# ===================== 练习题5：字符串常用方法 =====================
# 题目：给定字符串s = "  hello,ai,world  "，完成以下操作
# 1. 去除字符串首尾的空白字符
# 2. 将所有字符转为大写
# 3. 将字符串中的逗号替换为空格
# 4. 按逗号拆分字符串，得到一个列表并打印
# 请在此处编写代码：

s = "  hello,ai,world  "
clean_s = s.strip()
print(f"去除字符串首尾的空白字符：{clean_s}")
print(f"将所有字符转为大写：{clean_s.upper()}")
print(f"将字符串中的逗号替换为空格：{clean_s.replace(',', ' ')}")
lst = clean_s.split(",")
print(f"列表：{lst}")
print()

