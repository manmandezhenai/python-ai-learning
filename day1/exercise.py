# ===================== 练习题10：综合字符串处理 =====================
# 题目：给定日期字符串date_str = "2024-01-01"，完成以下操作
# 1. 按"-"拆分字符串，得到年、月、日三个部分的列表
# 2. 分别提取年、月、日，打印格式为：年：xxxx，月：xx，日：xx
# 3. 将日期重新拼接为"2024/01/01"的格式并打印
# 请在此处编写代码：

date_str = "2024-01-01"
lst = date_str.split("-")
print(f"列表：{lst}")
year, month, day = lst
print(f"年：{year}, 月：{month},日：{day}")
new_data = '/'.join(lst)
print(new_data)
print()